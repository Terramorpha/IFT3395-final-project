# ╔═╡ b9d98de7-f86b-4ee5-96c3-e2580565803b
function impurity(x)
	_, n_points = size(x)
	x̄ = mean(x, dims=2)
	centered_x = x .- x̄
	(repeat(x̄, 1, n_points) - x).^ 2 |> sum
end

# ╔═╡ d9662b26-7141-4a5e-9ab8-7cd01344c122
function split_impurity(partitions)
	ns = length(partitions)
	ps = ns ./sum(ns)
	impurity.(partitions) .* ps |> sum
end

# ╔═╡ 413eb052-0d51-46c5-ba7d-bd1eac752fa3
begin
	struct Const{T}
		x::Array{T}
	end
	@functor Const
	function (c::Const{T})(x) where T
		repeat(c.x, 1, size(x)[end])
	end
end

# ╔═╡ 43fb9666-7762-4548-bac0-2c33db79896d
begin
	struct Node
		feature::Int # le feature qu'on observe
		boundary::Float64 # la frontière de decésion
		left # si oui
		right # si non
	end
	@functor Node
	function (s::Node)(x)
		yesses = x[s.feature, :] .<= s.boundary
		yesses' .* s.left(x) .+ .! yesses' .* s.right(x)
	end
end

# ╔═╡ 6e3762d0-e311-49ee-b061-3481bec662b8
"""
Trouve le couple (feature, frontière) qui minimise l'impureté résiduelle
"""
function best_feature(input, output)
	n_features, n_samples = size(input)
	candidates = [
		(featurei, boundary) for featurei in 1:n_features
						     for boundary in begin
							 	 l = Set(input[featurei, :])
								 k = 20 
								 if length(l) >= 20
									sample(input[featurei, :], k)
								 else
									 l
								 end
							 end
	]
	choice = argmin(candidates) do (featurei, boundary)
		yesses = input[featurei, :] .<= boundary
		lefts = output[:, yesses]
		rights = output[:, .! yesses]
		split_impurity([lefts, rights])
	end
	choice
end

# ╔═╡ 1c6dcaa2-e672-4e65-bb8f-d8ac779386e4
"""
Construit un arbre de décision qui estime la valeur de l'output à partir de l'input
"""
function decision_tree(input, output; max_depth=0)
	if max_depth<=0 || size(input)[2] == 0
		Const(mean(output, dims=2))
	else
		featurei, boundary = best_feature(input, output)
		yesses = input[featurei, :] .<= boundary
		lefts = output[:, yesses]
		rights = output[:, .! yesses]
		
		left_leaf = decision_tree(input[:, yesses], lefts, max_depth=max_depth-1)
		right_leaf = decision_tree(input[:, .! yesses], rights, max_depth=max_depth-1)
		Node(featurei, boundary, left_leaf, right_leaf)
	end
end

# ╔═╡ dd580ae6-bee3-4d24-ace8-cd463fb38641
begin
	struct Lin # A linear combination of many functions
		weights
		parts
	end
	@functor Lin
	function (l::Lin)(x)
		map(l.weights, l.parts) do w, f
			w * f(x)
		end |> sum
	end
end

# ╔═╡ 47a18426-c0e6-4dfb-affa-f29bb4808d36
begin
	function boost(loss, input, output, trainweak; iter=5)
		base = Const(rand(size(output)[1]))
		data = DataLoader(
			(input = input,
			 output = output),
			batchsize = 32
		)
		# Optimise the initial constant prediction
		opt = Flux.setup(ADAM(1.0), base)
		Flux.train!(base, data, opt) do f, data
			loss(f(data.input), data.output)
		end

		# Then add `iter` additive layers
		f = boost(base, loss, input, output, trainweak, iter=iter)
		f
	end
	
	function boost(f, loss, input, output, trainweak; iter=1)
		# La prédiction de notre fonction
		weights = [1.0]
		funcs = [f]
		pred = f(input)
		@progress for i in 1:iter
			# On calcule le gradient de la fonction pour chaque point de données
			grad, = gradient(pred) do pred
				loss(pred, output)
			end
			
			# On fit un modèle faible au gradient négatif de l'image de la fonction
			weak = trainweak(input, -grad)
			weakpred = weak(input)
	
			eta = 50.0
			for i in 1:10
				etagrad, = gradient(eta, input) do eta, inp
					loss(pred + eta*weakpred, output)
				end
				etahess = hessian(eta) do eta
					loss(pred + eta*weakpred, output)
				end
				eta += -etagrad/(2etahess)
			end
			
			push!(funcs, weak)
			push!(weights, eta)
			pred += eta * weak(input)
		end
		Lin(weights, funcs)
	end
end

# ╔═╡ 6a44fa6c-36a5-471b-b1cc-e6b6b6910004
function loss(pred, target)
